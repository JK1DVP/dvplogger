// mcp_interface.cpp
#include "mcp.h"
#include "mcp_interface.h"

void mcp_write_pin(uint8_t pin, bool level) {
  mcp.digitalWrite(pin, level ? HIGH : LOW);
}

void mcp_pin_mode(uint8_t pin, uint8_t mode) {
  mcp.pinMode(pin,mode);
}
